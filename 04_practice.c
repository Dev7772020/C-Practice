# include <stdio.h>

int main()
{
    // Area Of Rectangle
    int length , breadth;

    printf("Enter the Length of region\n");
    scanf("%d" , &length);

    printf("Enter the breadth of region\n");
    scanf("%d" , &breadth);

    printf("Area of the Region is %d" , length*breadth);

    return 0;
}